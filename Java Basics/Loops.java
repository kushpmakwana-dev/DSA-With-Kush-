package com.kush;

public class Loops {
    public static void main(String[] args) {
        int count;
        count = 1;

        // While Loop
        while(count!=5){
            System.out.println(count);
            count++;
        }

        // For loop
        for(int counts = 1; counts <= 7; counts++ ){
            System.out.println(counts);
        }
    }
}
