package com.kush;

public class Primitives {
    public static void main(String[] args) {
        // Primitives - Cannot be broken any further
        int rollno = 64;
        char letter = 'r';
        float marks = 98.67f;
        double LargeDecimalNumber = 45678.6877444;
        long largeIntegers = 34566659666L;
        boolean check = false;
        /* Why do we add 'f' and 'L'? -> All decimals are by defaults in 'double' but to use float we add 'f'
                                         and same with 'L' because the by default number data type is 'int'.
        */

        // Non Primitives - Can be broken into another lower datatype
        String names = "Sanika";
    }
}
